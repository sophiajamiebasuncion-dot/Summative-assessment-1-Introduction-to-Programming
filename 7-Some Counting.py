print("Counting up from 0-50 in increments of 1")
for i in range(0, 51):
    print(i)

print("\nCounting down from 50-0 in decrements of 1")
for i in range(50, -1, -1):
    print(i)

print("\nCounting up from 30-50 in increments of 1")
for i in range(30, 51):
    print(i)

print("\nCounting down from 50-10 in decrements of 2")
for i in range(50, 9, -2):
    print(i)

print("\nCounting up from 100-200 in increments of 5")
for i in range(100, 201, 5):
    print(i)